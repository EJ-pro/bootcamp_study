# 2. 행렬 곱셈 수행
#    - 2×3 행렬 A와 3×2 행렬 B 생성
#    - A @ B 계산 (결과: 2×2 행렬)

import numpy as np

a = np.array([[1, 2, 3], [4, 5, 6]])
b = np.array([[7, 8], [9, 10], [11, 12]])

print("결과 값 : ",a@b)

print("수동 검증 : [ 1*7 + 2*9 + 3*11 ] => ", 1*7 + 2*9 + 3*11," [ 1*8 + 2*10 + 3*12 ] => ", 1*8 + 2*10 + 3*12)
print("수동 검증 : [ 4*7 + 5*9 + 6*11 ] => ", 4*7 + 5*9 + 6*11," [ 4*8 + 5*10 + 6*12 ] => ",  4*8 + 5*10 + 6*12)

